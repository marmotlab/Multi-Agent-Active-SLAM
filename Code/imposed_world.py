class World:
    def __OPEN__(self, map, poses) -> None:
        self.w_viewer = None
        self.map = map
        self.poses = poses

    def render_world(self):
        #variables
        screen_dim = (500,500)
        width = self.map.shape[1]
        height = self.map.shape[0]
        square_size = min(screen_dim[0]/width, screen_dim[1]/height)
        colors = {i_ag:hsv_to_rgb(np.array([i_ag/float(len(self.poses)),1,1])) for i_ag in range(len(self.poses))}

        if self.w_viewer == None:
            self.w_viewer = rendering.Viewer(screen_dim[0], screen_dim[1])

            #white screen background
            create_rectangle(self.w_viewer, 0, screen_dim[1], screen_dim[0], screen_dim[1], (1,1,1), permanent = True)
                
            #grey map background
            create_rectangle(self.w_viewer, 0, screen_dim[1], square_size*width, square_size*height,(.8,.8,.8), permanent = True)

            #drawing the map
            for i in range(height):
                start = 0
                end = 1
                scanning = False
                write = False
                for j in range(width):
                    pos = (i,j)
                    square = self.map[i,j]
                    if(square == 1 and not scanning): #obstacle found
                        start = j
                        scanning = True
                    if scanning:
                        if square == 0 :
                            end = j
                            scanning = False
                            write = True
                        elif j == width-1:
                            end = j+1
                            scanning = False
                            write = True
                    if write:
                        x = start*square_size
                        y = screen_dim[1] - i*square_size
                        create_rectangle(self.w_viewer, x, y, square_size*(end-start), square_size, (0.2,0.2,0.2), permanent = True)
                        write = False
            
        #drawing agents' position
        for i_ag in range(len(self.poses)):
            pos = self.poses[i_ag]
            x = pos[1]*square_size
            y = screen_dim[1] - pos[0]*square_size
            color = colors[i_ag]
            create_rectangle(self.w_viewer, x, y, square_size, square_size, color)
    
    def reset_viewer(self):
        self.w_viewer.close()
        self.w_viewer = None

